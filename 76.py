# Python program to demonstrate docstring in a function

def add(a, b):
    """This function returns the sum of two numbers."""
    return a + b

# Calling the function
result = add(5, 3)

# Printing result
print("Sum:", result)

# Displaying the docstring of the function
print("Function Docstring:", add.__doc__)


